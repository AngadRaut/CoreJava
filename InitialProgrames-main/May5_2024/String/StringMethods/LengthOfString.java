package May5_2024.String.StringMethods;

/** length() => no of characters in given string
 *            => no argument and returns length of string
 */
public class LengthOfString {
    public static void main(String[] args){
        String name="Java Programming";
        String name2=new String("Hellow");
        String name3="1234567890";
        System.out.println("length of name string is = "+name.length()+"\n" +
                "length of string name2 is = "+name2.length()+"\n" +
                "length of string name3 is = "+name3.length());

    }
}
